STAGE_HUMAN = {
    "": "не затрагивается",
    "идея": "идея (ещё ничего не построено)",
    "zaglushka": "заглушка «сайт в разработке»",
    "lending": "лендинг (одна страница)",
    "vitrina": "витрина (каталог из шаблонов и БД)",
    "magazin": "магазин (корзина, заказы, авторизация)",
    "zhivoy": "живой интерфейс (SPA, реактивность)",
    "integracii": "интеграции (оплата, доставка, 1С)",
    "prod": "прод (домен, хостинг, CI/CD)",
}


def main():
    created, skipped = 0, 0
    book_entries = []

    for mod_no, mod_slug, mod_title, mod_pain, pars in MODULES:
        mod_dir = os.path.join(MANUSCRIPT, f"{mod_no:02d}-{mod_slug}")
        os.makedirs(mod_dir, exist_ok=True)

        # README модуля — шапка главы
        mod_readme = os.path.join(mod_dir, "README.md")
        if not os.path.exists(mod_readme):
            with open(mod_readme, "w", encoding="utf-8") as f:
                f.write(f"# Модуль {mod_no}. {mod_title}\n\n")
                f.write(f"**Боль модуля:** {mod_pain}\n\n")
                f.write("Заставка модуля (что делаем, зачем, что будем "
                        "уметь после) — заполнить.\n\n## Параграфы\n\n")
                for par, slug, title, *_ in pars:
                    f.write(f"- §{par.replace('-', '.')} {title} "
                            f"— `{par}-{slug}.md`\n")
                f.write("\n## Резюме модуля\n\n— заполнить\n\n"
                        "## Большое практическое задание\n\n— заполнить\n\n"
                        "## Контрольная самопроверка\n\n— заполнить\n")
            created += 1

        for par, slug, title, pain, ancestor, stage, delta in pars:
            fname = f"{par}-{slug}.md"
            fpath = os.path.join(mod_dir, fname)
            rel = os.path.relpath(fpath, ROOT)
            book_entries.append(rel)
            if os.path.exists(fpath):
                skipped += 1
                continue
            with open(fpath, "w", encoding="utf-8") as f:
                f.write(PARAGRAPH_TEMPLATE.format(
                    module_no=mod_no,
                    par_dotted=par.replace("-", "."),
                    slug=slug,
                    title=title.replace('"', "'"),
                    pain=pain.replace('"', "'"),
                    ancestor=ancestor.replace('"', "'"),
                    stage=stage,
                    stage_human=STAGE_HUMAN.get(stage, stage),
                    example_delta=delta.replace('"', "'"),
                ))
            created += 1

    # book.yml — манифест сборки
    book_yml = os.path.join(ROOT, "book.yml")
    with open(book_yml, "w", encoding="utf-8") as f:
        f.write("# Манифест книги. Единственный источник порядка глав для\n")
        f.write("# сборки (pandoc). Регенерируется scripts/scaffold.py —\n")
        f.write("# руками не править список files, правьте манифест в скрипте.\n\n")
        f.write('title: "Веб глазами древнего человека"\n')
        f.write('subtitle: "Рабочее название — поменять"\n')
        f.write('author: "imiron.ru (iMironRU)"\n')
        f.write('language: ru\n')
        f.write('rights: "См. LICENSE"\n\n')
        f.write("example:\n")
        f.write(f'  name: "{EXAMPLE_NAME}"\n')
        f.write('  kind: "интернет-магазин книг"\n')
        f.write('  stages: [zaglushka, lending, vitrina, vitrina-blog, '
                'magazin, zhivoy, integracii, prod]\n\n')
        f.write("formats: [html, epub, pdf]\n\n")
        f.write("# Порядок файлов для склейки. Сгенерировано автоматически.\n")
        f.write("files:\n")
        for rel in book_entries:
            f.write(f'  - "{rel}"\n')

    print(f"Создано файлов: {created}, пропущено (уже были): {skipped}")
    print(f"Параграфов в манифесте: {len(book_entries)}")
    print(f"book.yml: {book_yml}")


if __name__ == "__main__":
    sys.exit(main())
